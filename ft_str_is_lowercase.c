#include "libft.h"

int		ft_str_is_lowercase(char *str)
{
	int i;
	int result;

	i = 0;
	result = 1;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			result = 1;
		else
			return (0);
		i++;
	}
	return (result);
}
